package NullConceptpackage;

public class OddEven {

	public static void main(String[] args) {

		int i;

		for (i = 0; i <= 10; i++) {

			// System.out.println(i);

			if (i % 2 == 0) {
				System.out.println(i + " : is Even number");
			} else {
				System.out.println(i + " : is Odd number");
			}

		}
	}

}
